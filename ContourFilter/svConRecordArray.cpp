// Initialize array.

void ConRecordArray::initialize(int aRows, int aCols)
{
   // Initialize the array memory and variables.
   BaseClass::initialize(aRows, aCols);

   // Reset each array element.
   SV::RCIndexLoop tLoop(4, 4);
   while (tLoop.loop())
   {
      BaseClass::at(tLoop()).reset();
   }
}

