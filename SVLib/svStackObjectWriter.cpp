void StackObjectWriter::initialize(StackObjectParms* aParms)
{
   // Store parms.
   mP = aParms;

}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Write a stack object slice to an output image, based on a
// stack index, according to the parms.

void StackObjectWriter::doWriteStackObject(
   int            aStackIndex,     // Control
   cv::Mat&       aOutputImage)    // Output
{
   Prn::print(0, "StackObjectWriter::doWriteStackObject");

   // Initialize the tile writer.
   mTileWriter.initialize(&mP->mTileParmsA);

   // Write a tile to the output image at the stack index.
   int tNumLoop = aStackIndex;
   mTileWriter.doWrite(tNumLoop,aOutputImage);
}

