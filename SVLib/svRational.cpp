// Convert to string for prints.
char* Rational::asString(char* aString)
{
   sprintf(aString,"%d/%d",mNumer,mDenom);
   return aString;
}

// Read command line command arguments.
void Rational::readArgs(Ris::CmdLineCmd* aCmd)
{
   mNumer = 0;
   mDenom = 1;

   char tString[40];
   aCmd->copyArgString(1,tString,40);

   char* tToken = strtok(tString,"/");
   if (tToken)
   {
      mNumer = atoi(tToken);
   }
   else
   {
      return;
   }
   
   tToken = strtok(0,"/");
   if (tToken)
   {
      mDenom = atoi(tToken);
   }
}

