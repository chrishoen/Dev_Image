//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constructor.

ImageResults::ImageResults()
{
   reset();
}

void ImageResults::reset()
{
   mFirst = true;
   mTotalCount = 0;
   mHighCount = 0;

   mMinRow = 0;
   mMaxRow = 0;
   mMinCol = 0;
   mMaxCol = 0;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Show.

void ImageResults::show(int aPF, const char* aLabel)
{
   Prn::print(aPF, "%7d ROW %4d %4d COL %4d %4d    %s",
      mHighCount,
      mMinRow, mMaxRow, mMinCol, mMaxCol,
      aLabel);
}

