#include "StackDisplay.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constructor.

StackDisplay::StackDisplay()
{
   reset();
}

void StackDisplay::reset()
{
   mReadCount = 0;
}


//******************************************************************************
//******************************************************************************
//******************************************************************************
// Test a script file. Return true if successful.

bool StackDisplay::doTestScriptFile(std::string& aScriptFilePath)
{
   // Do this first.
   reset();

   // Open the script file.
   if (!mReader.doOpenFile(aScriptFilePath)) return false;

   // Loop to test the script file.
   while (true)
   {
      // Read from the file. Exit if end of file.
      if (!mReader.doRead()) break;
      mReadCount++;

      // Test the command code.
      if (mReader.mCmdCode == PX::cScriptCmd_Slice)
      {
         // Display image.
         Display::showImageFullPath(mReader.mString);
      }
   }

   // Done.
   mReader.doCloseFile();
   return true;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Show test results.

void StackDisplay::show()
{
   return;
   Prn::print(0, "");
   Prn::print(0, "StackDisplay Results****************");
   Prn::print(0, "ReadCount        %5d", mReadCount);
}

