ImageFilterGaussBlur::ImageFilterGaussBlur()
{
   reset();
}

ImageFilterGaussBlur::ImageFilterGaussBlur(ImageFilterParms* aParms)
{
   BaseClass::mP = aParms;
   reset();
}

void ImageFilterGaussBlur::reset()
{
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
//******************************************************************************
//******************************************************************************
//******************************************************************************
// Filter the image, depending on the parms.

void ImageFilterGaussBlur::doFilterImage(
   cv::Mat&       aInputImage,     // Input
   cv::Mat&       aOutputImage)    // Output
{
   Prn::print(0, "ImageFilterGaussBlur::doFilterImage");

   // Filter the input image to the output image.
   cv::GaussianBlur(
      aInputImage,
      aOutputImage,
      cv::Size(mP->mGaussBlurCode, mP->mGaussBlurCode),
      mP->mGaussBlurSigma,
      mP->mGaussBlurSigma,
      cv::BORDER_DEFAULT);
}

